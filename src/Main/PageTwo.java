        Main.H1.getS1()[Hostel.studentsCounter].setTotalBillPerMonth(Main.H1.getR1()[Main.H1.getS1()[Hostel.studentsCounter].getRoomNoAssigned()].getCharges());
        Main.H1.getS1()[Hostel.studentsCounter].setTotalAdvancePayed(Double.parseDouble(JOptionPane.showInputDialog(this, "Enter total advance amount: ")));
        Main.H1.getS1()[Hostel.studentsCounter].setId(Hostel.studentsCounter+1);
