    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
        int A = Integer.parseInt(JOptionPane.showInputDialog(Main.P2, "Enter room id to delete: "));
        if (Main.H1.isSearchRoom(A))
        {
            int Index = Main.H1.searchRoom(A);
            JOptionPane.showMessageDialog(this, "Room found & deleted successfully!");
            for (int i=0;i<Hostel.roomsCounter;i++)
            {
                Main.H1.getR1()[i] = Main.H1.getR1()[i+1];
            }
            Hostel.roomsCounter-=1;
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Sorry, no room found!");          
        }
    }//GEN-LAST:event_jButton13ActionPerformed

